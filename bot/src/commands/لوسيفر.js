// إرسال مباشر متجاوز للـ wrappers لضمان الوصول
function directSend(threadID, msg, callback) {
  try {
    const api = global.api;
    if (!api) { if (callback) callback(new Error("api غير متاح")); return; }
    api.sendMessage(msg, threadID, callback || (() => {}));
  } catch (e) {
    console.error(`[لوسيفر] خطأ directSend: ${e.message}`);
    if (callback) callback(e);
  }
}

